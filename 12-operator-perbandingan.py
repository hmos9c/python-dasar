# Operator Perbandingan

# > lebih dari
# < kurang dari
# >= lebih dari sama dengan
# <= kurang dari sama dengan

# == sama dengan, bisa di gunakan untuk variable
# != tidak sama dengan, bisa di gunakan untuk variable

print(1 > 4) # False
print(1 < 4) # True
print(1 < 1) # False
print(1 <= 1) # True

print(10 == 10) # True
print(10 != 9) # True

print("sanas" == "sanas") # true
print("sanas" != "sanas") # False