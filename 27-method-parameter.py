# Belajar Method Parameter

def say_hello(name):  # (parameter)
    print(f"Hello {name}")


say_hello("sanas")
say_hello("akmal")


def say_hello(first_name, last_name):  # (parameter 1, parameter 2)
    print(f"Hello {first_name} {last_name}")


say_hello("sanas", "febriyan")
say_hello("muhammad", "akmal")
